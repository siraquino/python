# Ejercicio resuelto N001
# programa: Calcular el perímetro de un cuadrado sabiendo que todos
# sus lados miden 4 centímetros.
# Nota: El perímetro de un cudrdo es igual a la suma de sus lados
print("Programa N001: Determinar Perímetro de un cuadrado")
lado = 4
perimetro = lado + lado + lado + lado
# Aquí puedes resolver de distintas maneras
#Te aconsejo no usar acentos
print("El perímetro del cuadrado es: ", perimetro, "centímetros")
print()
