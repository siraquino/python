# Ejercicio resuelto N003
# Programa: Se desea saber que edad tendrá una persona en el año
# 2030 sabiendo que nació en el año 2009.
print('Programa N003: Determinar la edad de una persona')
nacio = 2009
futuro = 2030
edad = futuro - nacio
print('Tendrá ', edad, 'años de edad')
