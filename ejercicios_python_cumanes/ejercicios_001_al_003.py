# Ejercicio resuelto N002
# programa: De los 365 días del año han transcurrido 120 días.
# ¿Cuántos días faltan para que termine el año? 
print("Programa N002: Determinar cuantos días faltan")
total_dias = 365
dias_trans = 120
faltan = total_dias - dias_trans
print("Faltan", faltan, "días para que termine el año")
print()
